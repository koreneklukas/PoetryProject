class Application:

	def math_operation_summation(self, a, b) -> int:
		summation = a + b
		return summation

	def math_operation_division(self, a, b) -> int:
		division = a / b
		return division

	def math_operation_multiplication(self, a, b) -> int:
		multiplication = a * b
		return multiplication

	def math_operation_subtraction(self, a, b) -> int:
		subtraction = a - b
		return subtraction
